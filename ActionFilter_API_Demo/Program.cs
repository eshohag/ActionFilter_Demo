using ActionFilter_API_Demo.Filters;

namespace ActionFilter_API_Demo
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews(options =>
            {
                options.Filters.Add<GlobalSampleActionFilter>();
            });
            builder.Services.AddScoped<IGlobalSampleActionFilterService, GlobalSampleActionFilterService>();

            var app = builder.Build();

            // Configure the HTTP request pipeline.

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
